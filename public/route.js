app.config(function($routeProvider){
  $routeProvider
  .when("/",{
    templateUrl:"pages/main.html",
    controller:"mainCtrl"
  })
  .when("/register",{
    templateUrl:"pages/register.html",
    controller:"mainCtrl"
  })
    .when("/login",{
      templateUrl:"pages/login.html",
      controller:"mainCtrl"
    });
});
